package praktikum;

public class OperasiMatematika {
    public static void main(String[] args) {
        int x = 2, y = 3;
        System.out.println("Penjumlahan = " + (x + y));
        System.out.println("Perkalian   = " + (x * y));
        System.out.println("Pembagian   = " + ((double) x / y));
    }
}
